inline static void crc8_update(uint8_t * accamulated, uint8_t elem)
{
   *accamulated = crc8_table[(*accamulated) ^ elem];
}


uint8_t crc8(const void * data, size_t data_size)
{
   uint8_t accamulated = 0;
   const uint8_t * data_ptr = (const uint8_t*)data;
   for (size_t i = 0; i < data_size; i++)
   {
      crc8_update(&accamulated, data_ptr[i]);
   }

   return accamulated;
}

